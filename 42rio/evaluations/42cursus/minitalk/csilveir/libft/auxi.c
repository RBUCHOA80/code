#include "ft_printf.h"

int	ft_putchar(char c)
{
	int	i;

	i = 1;
	write(1, &c, 1);
	return (i);
}

int	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (str == NULL)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (*(str + i) != '\0')
	{
		i += ft_putchar(*(str + i));
	}
	return (i);
}
