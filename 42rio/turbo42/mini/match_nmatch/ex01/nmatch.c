int	nmatch(char *s1, char *s2)
{
	int	s1_len;
	int	s2_len;

	s1_len = 0;
	while (s1[s1_len])
		s1_len++;
	s2_len = 0;
	while (s2[s2_len])
		s2_len++;
	return (0);
}
