#include <stdio.h>

char	*ft_strlowcase(char *str);

int	main(void)
{
	char	str[] = "PuT YoUr SaMpLe TeXt HeRe!";

	printf("str = %s\n", str);
	ft_strlowcase(str);
	printf("str = %s <- ft_strlowercase\n", str);
	return (0);
}

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if ('A' <= str[i] && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	return (str);
}
