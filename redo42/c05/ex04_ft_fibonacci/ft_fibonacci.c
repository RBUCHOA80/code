#include <stdio.h>

int	ft_fibonacci(int index);

int	main(void)
{
	int	index;

	index = 12;
	printf("%d\n", ft_fibonacci(index));
	return (0);
}

int	ft_fibonacci(int index)
{
	if (index < 0)
		return (-1);
	else if (index < 2)
		return (index);
	else
		return (ft_fibonacci(index - 1) + ft_fibonacci(index - 2));
}
